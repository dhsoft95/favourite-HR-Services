<script>
    document.addEventListener('DOMContentLoaded', function() {
        const slider = document.getElementById('jobsSlider');
        const prevBtn = document.getElementById('prevBtn');
        const nextBtn = document.getElementById('nextBtn');
        const dots = document.querySelectorAll('.dot-indicator');

        let currentSlide = 0;
        let itemsPerView = 3; // Default for large screens
        let totalCards = 5;

        // Update items per view based on screen size
        function updateItemsPerView() {
            if (window.innerWidth < 768) {
                itemsPerView = 1;
            } else if (window.innerWidth < 1024) {
                itemsPerView = 2;
            } else {
                itemsPerView = 3;
            }
        }

        // Update slider position
        function updateSlider() {
            const slideWidth = 100 / itemsPerView;
            const translateX = -currentSlide * slideWidth;
            slider.style.transform = `translateX(${translateX}%)`;

            // Update dots
            dots.forEach((dot, index) => {
                if (index === currentSlide) {
                    dot.classList.add('bg-indigo-900');
                    dot.classList.remove('bg-gray-300');
                } else {
                    dot.classList.add('bg-gray-300');
                    dot.classList.remove('bg-indigo-900');
                }
            });
        }

        // Next slide
        function nextSlide() {
            const maxSlides = Math.max(0, totalCards - itemsPerView);
            if (currentSlide < maxSlides) {
                currentSlide++;
            } else {
                currentSlide = 0; // Loop back to start
            }
            updateSlider();
        }

        // Previous slide
        function prevSlide() {
            const maxSlides = Math.max(0, totalCards - itemsPerView);
            if (currentSlide > 0) {
                currentSlide--;
            } else {
                currentSlide = maxSlides; // Loop to end
            }
            updateSlider();
        }

        // Event listeners
        nextBtn.addEventListener('click', nextSlide);
        prevBtn.addEventListener('click', prevSlide);

        // Dot navigation
        dots.forEach((dot, index) => {
            dot.addEventListener('click', () => {
                currentSlide = index;
                updateSlider();
            });
        });

        // Auto-play (optional)
        setInterval(nextSlide, 5000);

        // Handle resize
        window.addEventListener('resize', () => {
            updateItemsPerView();
            currentSlide = 0; // Reset to first slide on resize
            updateSlider();
        });

        // Initialize
        updateItemsPerView();
        updateSlider();
    });
</script>
